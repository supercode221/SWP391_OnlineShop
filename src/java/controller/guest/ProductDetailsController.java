    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HoangAnhCustomEntityDAO ceDAO = new HoangAnhCustomEntityDAO();
        HttpSession session = request.getSession();
        int productId = Integer.parseInt(request.getParameter("productId"));

        if (session.getAttribute("uid") != null) {
            int uid = (int) session.getAttribute("uid");
            if (ceDAO.isValidWishListItem(productId, uid)) {
                request.setAttribute("validWish", "valid");
            }
        }

        // Get the message and product details
        String msg = request.getParameter("msg"); // This will retrieve the URL parameter msg
        request.setAttribute("msg", msg); // Store it as an attribute
        request.setAttribute("productDetail", ceDAO.getDetailProductbyId(productId));
        request.setAttribute("attributeList", ceDAO.getProductAttributeById(productId));
        request.setAttribute("feedback", ceDAO.getFeedbackDetailsListByProductId(productId));
        if (ceDAO.getFeedbackMediaByProductId(productId) != null) {
            request.setAttribute("feedbackMedia", ceDAO.getFeedbackMediaByProductId(productId));
        }
        request.setAttribute("productId", productId);

        // Forward to the JSP
        request.getRequestDispatcher("public/ProductDetail.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        doGet(request, response);
    }

